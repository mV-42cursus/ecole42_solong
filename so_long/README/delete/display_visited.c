void	display_visited(t_map *data) 
{
	////////////////////////////////////////////////////
	////////////////////////////////////////////////////
	int row_idx = 0;
	int col_idx = 0;
	while (row_idx < data->row)
	{
		col_idx = 0;
		while (col_idx < data->col)
		{
			printf("%d ", data->visited[row_idx][col_idx]);
			col_idx++;
		}
		puts("");
		row_idx++;
	}
	puts("");
	////////////////////////////////////////////////////
	////////////////////////////////////////////////////
}
